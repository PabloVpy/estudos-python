n = input('Digite algo para identificação:')
print(type(n))

print('Ele é numérico?')
print(n.isnumeric())

print('Ele é decimal?')
print(n.isdecimal())

print('Ele é letra?')
print(n.isalpha())

print('Ele é alfanumérico?')
print(n.isalnum())

print('Ele é maiúsculo?')
print(n.isupper())

print('Ele é minúsculo?')
print(n.islower())
